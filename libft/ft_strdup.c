#include "libft.h"

char	*ft_strdup(const char *src)
{
	char			*str;
	unsigned int	len;
	int				i;

	i = 0;
	len = ft_strlen(src);
	if (!(str = (char*)malloc(sizeof(*str) * (len + 1))))
		return (NULL);
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
